void optimizedRotateMergeSort(void *array, size_t nelements, size_t elemsize, MERGECMP *cmp)
{
	if (nelements < 2)
	{
		return;
	}

	char ext[MERGEEXTSIZE];
	switch (elemsize)
	{
		case sizeof(char):
			return rotateMergeSort8(array, nelements, (char*)ext, cmp);

		case sizeof(short):
			return rotateMergeSort16(array, nelements, (short*)ext, cmp);

		case sizeof(int):
			return rotateMergeSort32(array, nelements, (int*)ext, cmp);

		case sizeof(long long):
			return rotateMergeSort64(array, nelements, (long long*)ext, cmp);

#ifndef _WIN32
		case sizeof(long double):
			return rotateMergeSort128(array, nelements, (long double*)ext, cmp);
#endif

		default:
			return assert(elemsize == sizeof(char) || elemsize == sizeof(short) || elemsize == sizeof(int) || elemsize == sizeof(long long) || elemsize == sizeof(long double));
	}
}

